package Method_int_ReturnType;

public class int1 {
	
	public static int m1 ()
	{
		return 10;
		
	}
	
	public static int m2 ()
	{
		return 20;
		
	}
	
	public static int m3 ()
	{
		return 30;
		
	}
	
	public static int m4 ()
	{
		return 40;
		
	}
	
	public static int m5 ()
	{
		return 50;
		
	}
	
		
	public static void main(String[] args) {


		System.out.println(int1.m1());
		System.out.println(int1.m2());
		System.out.println(int1.m3());
		System.out.println(int1.m4());
		System.out.println(int1.m5());

		
		
	}

}







