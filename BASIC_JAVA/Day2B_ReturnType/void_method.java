package Day2B_ReturnType;

public class void_method {
	
	void A1 () {
		
		int a =10;
		System.out.println(a);
	}
	
	void A2() {
		
		int b =20;
		System.out.println(b);
	}
	
	void A3() {
		
		int c = 30;
		System.out.println(c);
	}
	
	
	void A4( ) {
		
		int d =40;
		System.out.println(d);
	}
	

	public static void main(String[] args) {

		
		void_method obj = new void_method ();
		
		obj.A1();
		obj.A2();
		obj.A3();
		obj.A4();
		
		
		
		
		
		
	}

}
