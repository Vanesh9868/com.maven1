package Day4_ReturnMethod;
               
public class Return_A {
			                                                
public static byte  yogesh   () 
     {
	
	
	     return 10;
     }     

public static int   shubham  ()    
    {
	
	
	   return 100;
   } 

public static char  anil     ()
     { 

	
	    return 200; 
     } 

public static void main(String[] args) {
                       	  
    	  int X = Return_A.yogesh();      
    	  int Y = Return_A.shubham();      
    	  int Z = Return_A.anil();      

 	       System.out.println(X);            
    	   System.out.println(Y);
    	   System.out.println(Z);
		
	}

}
