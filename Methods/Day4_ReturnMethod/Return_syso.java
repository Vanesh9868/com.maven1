package Day4_ReturnMethod;

public class Return_syso {
	
	
public String yogesh  ()
     {
	
	    System.out.println("yogesh");
	     
	    return null;    
     }

public String shubham ()
     {
	      
         System.out.println("shubham");
	   
         return null;
	 }

public String anil    ()     
     {
	      
         System.out.println("anil");
	    
         return null ;
	 }


	public static void main(String[] args) {

		Return_syso obj = new Return_syso ();
		
		obj.yogesh();
		obj.shubham();
		obj.anil();
		
		
		
	}

}
