package Day4_Methods_inJava;       //static method

public class static_method {
	
// access modifier - return type - method name () {    ;}
	
public static void cat   () {System.out.println("eat milk ");}   

public static void horse () {System.out.println("run fast");}

public static void birds () {System.out.println("fly in sky");}


public static void main(String[] args) { 

	    static_method.cat  ();               //class name.method name();
		static_method.horse ();
		static_method.birds ();
		

	}

}

